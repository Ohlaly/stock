    private void jLabel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel15MouseClicked
        File f=new File(fname);
        InputStream is=null;
        try {
            is = new FileInputStream(f);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(GestProdForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        if(prenom.getText().isEmpty()==true || nom.getText().isEmpty()==true || phone.getText().isEmpty()==true || mail.getText().isEmpty()==true)
        {
            JOptionPane.showMessageDialog(null,"Remplissez les informations");
        }
        else
            {
            String lastname=prenom.getText();
        String fn=nom.getText();
        String tele=phone.getText();
        String em=mail.getText();
        
        try{
            String sql = "insert into fournisseur values(null,'"+is+"','"+lastname+"','" +fn + "','" + tele + "','"+em+"')";
            
            Statement st = connexion.getConnection().createStatement();
            st.executeUpdate(sql);
            images.setIcon(null);
            prenom.setText("");
            nom.setText("");
            phone.setText("");
            mail.setText("");
            JOptionPane.showMessageDialog(null,"Fournisseur ajoute avec succes");
            
        }catch(Exception e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"fournisseur non ajoute");
        }
        }
    }//GEN-LAST:event_jLabel15MouseClicked

