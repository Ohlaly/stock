    private void impActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_impActionPerformed
        JFileChooser fchoser=new JFileChooser();
        FileNameExtensionFilter fnef=new FileNameExtensionFilter("PNG JPG JPEG","png","jpeg","jpg");
        fchoser.addChoosableFileFilter(fnef);
        int load=fchoser.showOpenDialog(null);
        if(load==fchoser.APPROVE_OPTION)
        {
            File f=fchoser.getSelectedFile();
            fname=f.getAbsolutePath();
            ImageIcon micon=new ImageIcon(fname);
            Image img=micon.getImage().getScaledInstance(100,100,Image.SCALE_SMOOTH);
            images.setIcon(new ImageIcon(img));
        }
    }//GEN-LAST:event_impActionPerformed

