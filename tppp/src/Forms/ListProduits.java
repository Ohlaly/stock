    private void jLabel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel15MouseClicked
        model.setRowCount(0);
        if(Choice.getSelectedItem()=="Categorie")
       {
           if(search.getText().isEmpty())
            {
                loadProduits();
            }
            else
           {
           for(Produit p:ps.findByCategory(search.getText()))
           {
               model.addRow(new Object[]{
                p.getId(),
                p.getImage(),
                p.getDesignation(),
                p.getCategor(),
                p.getPrixAchat(),
                p.getTva(),
                p.getQuantite(),
                p.getRayon()
            });
           }
           }
           
           }
        else if (Choice.getSelectedItem()=="Id")
        {
            if(search.getText().isEmpty())
            {
                loadProduits();
            }
            else
            {
            int d=Integer.valueOf(search.getText());
            Produit p=ps.findById(d);
            model.addRow(new Object[]{
                p.getId(),
                p.getImage(),
                p.getDesignation(),
                p.getCategor(),
                p.getPrixAchat(),
                p.getTva(),
                p.getQuantite(),
                p.getRayon()
            });
        }
        }
        else if (Choice.getSelectedItem()=="Designation")
        {
            if(search.getText().isEmpty())
            {
                loadProduits();
            }
            else
            {
            for(Produit p:ps.findByDesign(search.getText()))
            {
               model.addRow(new Object[]{
                p.getId(),
                p.getImage(),
                p.getDesignation(),
                p.getCategor(),
                p.getPrixAchat(),
                p.getTva(),
                p.getQuantite(),
                p.getRayon()
            }); 
            }
            }
        }
        else if (Choice.getSelectedItem()=="Prix")
        {
            if(search.getText().isEmpty())
            {
                loadProduits();
            }
            else
            {
            double pl=Double.valueOf(search.getText());
            for(Produit p:ps.findByPrix(pl))
            {
                model.addRow(new Object[]{
                p.getId(),
                p.getImage(),
                p.getDesignation(),
                p.getCategor(),
                p.getPrixAchat(),
                p.getTva(),
                p.getQuantite(),
                p.getRayon()
            }); 
            }
            }
        }
        else if(Choice.getSelectedItem()=="Rayon")
        {
            if(search.getText().isEmpty())
            {
                loadProduits();
            }
            else
            {
            for(Produit p:ps.findByRayon(search.getText()))
            {
                model.addRow(new Object[]{
                p.getId(),
                p.getImage(),
                p.getDesignation(),
                p.getCategor(),
                p.getPrixAchat(),
                p.getTva(),
                p.getQuantite(),
                p.getRayon()
            }); 
            }
        }
        }
        
    }//GEN-LAST:event_jLabel15MouseClicked

