    public void loadProduits()
    {
        model.setRowCount(0);
        for(Produit p:ps.findAll())
        {
            model.addRow(new Object[]{
                p.getId(),
                p.getImage(),
                p.getDesignation(),
                p.getCategor(),
                p.getPrixAchat(),
                p.getTva(),
                p.getQuantite(),
                p.getRayon()
            });
        }
    }
    public void loadCategory()
    {
        
        for(Category c:cs.findAll())
        {
            Catego.addItem(c);
          
        }
    }
    public void loadRayon()
    {
        
        for(Rayon r:rs.findAll())
        {
            Ray.addItem(r);
        }
    }

