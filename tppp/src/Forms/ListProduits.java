    private void jLabel17MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel17MouseClicked
        
        Category categor=(Category)Catego.getSelectedItem();
        String designe=design.getText();
        Double prix=Double.parseDouble(prixAchat.getText());
        Double tv=Double.parseDouble(Tva.getText());

        Rayon r=(Rayon) Ray.getSelectedItem();

        try{
            String sql = "update produit set Designation='"+designe+"',categor=" + categor.getId() + ",prixAchat=" + prix + ",TVA="+tv+",rayon="+
             r.getId() + " where id="+id;
            Statement st = connexion.getConnection().createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(this,"Produit mis a jour avec succes");
            loadProduits();
        }catch(Exception e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,"produit non modifie");
        }
    }//GEN-LAST:event_jLabel17MouseClicked

